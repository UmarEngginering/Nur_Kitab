bacaan = []
for line in raw_text.split('\n'):
    line = line.strip()
    if not line:
        continue
    if '✿' in line:
        parts = line.split('✿')
        bacaan.append({
            'bait_kanan': parts[0].strip(),
            'bait_kiri': parts[1].strip()
        })
    else:
        bacaan.append(line)

new_maulid = {
    'id': 3,
    'judul': 'Mahallul Qiyam Barzanji',
    'tipe': 'maulid',
    'bacaan': bacaan
}

with open('c:/Users/DELL/flutter_uas/nur_kitab/asset/json/maulid.json', 'r', encoding='utf-8') as f:
    data = json.load(f)

# cek apakah sudah ada
found = False
for item in data['data']:
    if item['judul'] == 'Mahallul Qiyam Barzanji':
        item['bacaan'] = bacaan
        found = True
if not found:
    data['data'].append(new_maulid)

with open('c:/Users/DELL/flutter_uas/nur_kitab/asset/json/maulid.json', 'w', encoding='utf-8') as f:
    json.dump(data, f, ensure_ascii=False, indent=2)

print('Success adding Mahallul Qiyam Barzanji')
