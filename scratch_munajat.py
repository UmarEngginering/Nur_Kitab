def update_file(filepath, tipe_name):
    with open(filepath, 'r', encoding='utf-8') as f:
        data = json.load(f)
    
    # check if Munajat is already added
    if any(item.get('judul') == 'Munajat' for item in data['data']):
        print(f"Munajat already exists in {filepath}")
        return

    new_id = len(data['data']) + 1
    new_entry = {
        "id": new_id,
        "judul": "Munajat",
        "tipe": tipe_name,
        "bacaan": munajat_bacaan
    }
    
    data['data'].append(new_entry)
    
    with open(filepath, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    print(f"Added Munajat to {filepath}")

