def parse_content(content):
    parsed = []
    lines = content.strip().split('\n')
    for line in lines:
        line = line.strip()
        if not line: continue
        if '۞' in line:
            parts = line.split('۞')
            parsed.append({
                "bait_kanan": parts[0].strip(),
                "bait_kiri": parts[1].strip()
            })
        else:
            parsed.append(line)
    return parsed

